import re

class NamedColors:

    by_name = {}
    def __init__(self, r, g, b, a=None,name=None):
        self.red = r
        self.green = g
        self.blue = b
        self.alpha = a
        self.name = name
        if name != None:
            NamedColors.by_name[name] = self
    def to_tuple(self):
        return (self.red,self.green,self.blue)
    @staticmethod
    def to_color(name):
        if NamedColors.by_name.__contains__(name):
            return NamedColors.by_name[name]
        if name.startswith('rgb'):
            p = re.compile('\\d+')
            v = p.findall(name)
            return NamedColors(int(v[0]),int(v[1]),int(v[2]))
